package org.dakr.service;

public class CalculatorService {

    public static int sumofTwoNumber(int a,int b){
        return a+b;
    }

    public static int subtractionOfTwoNumber(int a,int b){
        return a-b;
    }

    public static int productOfNumber(int a,int b){
        return a*b;
    }

    public static double divOfNumber(int a,int b){
        return a/b;
    }
}
